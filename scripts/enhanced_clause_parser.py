@dataclass
class CompanyInfo:
    name: str = ""
    unified_credit_code: str = ""
    legal_representative: str = ""
    registered_capital: str = ""
    paid_capital: str = ""
    address: str = ""
    bank_name: str = ""
    bank_account: str = ""
    contact: str = ""
    phone: str = ""


@dataclass
class TradeSecretInfo:
    """Trade secret protection analysis result"""
    has_confidentiality_clause: bool = False
    confidentiality_scope_defined: bool = False
    confidentiality_period_defined: bool = False
    confidentiality_period_reasonable: bool = True
    confidentiality_measures_defined: bool = False
    non_compete_present: bool = False
    non_compete_period_months: int = 0
    non_compete_compensation: bool = False
    return_or_destroy_clause: bool = False
    breach_remedies_defined: bool = False
    risk_score: int = 0
    findings: List[str] = None

    def __post_init__(self):
        if self.findings is None:
            self.findings = []


@dataclass
class Clause:
    number: str = ""
    title: str = ""
    content: str = ""
    category: str = ""
    risk_level: str = "LOW"
    risk_score: int = 0
    risk_factors: List[str] = None
    legal_references: List[str] = None
    suggestions: List[str] = None
    article_ref: str = ""  # Civil Code article reference

    def __post_init__(self):
        if self.risk_factors is None:
            self.risk_factors = []
        if self.legal_references is None:
            self.legal_references = []
        if self.suggestions is None:
            self.suggestions = []


